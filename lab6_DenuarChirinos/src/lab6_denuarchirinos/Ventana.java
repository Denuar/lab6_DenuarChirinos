    private void btn_ayudaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ayudaActionPerformed
        // TODO add your handling code here:
        txt_pass2.setText("");
        ArrayList <Integer> minusculas = new ArrayList();
        ArrayList <Integer> mayusculas = new ArrayList();
        for (int i = 65; i < 91; i++) {
            mayusculas.add(i);
        }
        for (int i = 97; i < 123; i++) {
            minusculas.add(i);
        }
        for (int i = 0; i < pass.length(); i++) {
            char letra = pass.charAt(i);
            int let = (int) letra;
            int suma = let+llave;
            if (let>=65 && let<=90){//si la letra es mayuscula
                if (suma>90){
                    suma = suma-90;
                    suma = suma+96;
                }
            }
            else if (let>=97 && let<=122){//si es minuscula
                if (suma>122){
                    suma = suma-122;
                    suma = suma+64;
                }
            }
            //System.out.println(suma);
            char letra2= (char) suma;
            pass2+=letra2;
        }
        txt_pass2.setText(pass2);
    }//GEN-LAST:event_btn_ayudaActionPerformed

